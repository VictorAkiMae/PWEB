num1 = prompt("Informe o primeiro valor");
num2 = prompt("Informe o segundo valor");

alert("Resultado da soma dos dois:  " + " " + (parseFloat(num1) + parseFloat(num2)));
alert("Subtração do primeiro pelo segundo:" + " " + (parseFloat(num1) - parseFloat(num2)));
alert("Resultado do produto dos dois números:" + " " + parseFloat(num1) * parseFloat(num2));
alert("Resultado da divisao do primeiro pelo segundo:" + " " + parseFloat(num1) % parseFloat(num2));
alert("Resultado do resto da divisão do peimeiro pelo segundo:" + " " + (parseFloat(num1) / parseFloat(num2)));